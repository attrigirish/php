<?php

//Inserting an Element at the beginning
$arr=array(43,65,34,76);
array_unshift($arr,99);
print_r($arr);
echo "<br>";


//Deleting an Element from the beginning
$arr=array(43,65,34,76);
array_shift($arr);
print_r($arr);
echo "<br>";


//Inserting an Element at the end of an array
$arr=array(43,65,34,76);
array_unshift($arr,99);
print_r($arr);


?>